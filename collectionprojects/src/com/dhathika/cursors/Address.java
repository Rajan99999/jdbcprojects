package com.dhathika.cursors;

public class Address {
	
	int hNo;
	String village;
	String state;
	String country;
	public Address(int hNo, String village, String state, String country) {
		super();
		this.hNo = hNo;
		this.village = village;
		this.state = state;
		this.country = country;
	}
	
	

}
